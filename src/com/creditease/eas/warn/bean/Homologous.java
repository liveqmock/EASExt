	private String fpersonname;
	private String fpersonemail;
	private String fnumber;
	private Integer ftype;
	
	public Homologous(){
		
	}

	public Integer getFid() {
		return fid;
	}

	public void setFid(Integer fid) {
		this.fid = fid;
	}

	public String getFdepartmentname() {
		return fdepartmentname;
	}

	public void setFdepartmentname(String fdepartmentname) {
		this.fdepartmentname = fdepartmentname;
	}

	public String getFpersonname() {
		return fpersonname;
	}

	public void setFpersonname(String fpersonname) {
		this.fpersonname = fpersonname;
	}

	public String getFpersonemail() {
		return fpersonemail;
	}

	public void setFpersonemail(String fpersonemail) {
		this.fpersonemail = fpersonemail;
	}

